using ContactManagerApi.Domain.Entities;
using ContactManagerApi.Domain.Interfaces;
using Microsoft.EntityFrameworkCore;

namespace ContactManagerApi.Infrastructure.Repositories
{
    public class ContactRepository : IContactRepository
    {
        private readonly AppDbContext _context;

        public ContactRepository(AppDbContext context)
        {
            _context = context;
        }

        public async Task<List<Contact>> GetAllContactsAsync() => await _context.Contacts.ToListAsync();

        public async Task<Contact> GetContactByIdAsync(int id) => await _context.Contacts.FindAsync(id);

        public async Task AddContactAsync(Contact contact)
        {
            _context.Contacts.Add(contact);
            await _context.SaveChangesAsync();
        }

        public async Task UpdateContactAsync(Contact contact)
        {
            _context.Contacts.Update(contact);
            await _context.SaveChangesAsync();
        }

        public async Task DeleteContactAsync(int id)
        {
            var contact = await _context.Contacts.FindAsync(id);
            if (contact != null)
            {
                _context.Contacts.Remove(contact);
                await _context.SaveChangesAsync();
            }
        }
    }

}
